export function getTeamFlag(name: string): string {
  const code = TEAM_COUNTRY_CODES[name];
  if (!code) return '🏳️';
  if (code === 'GB-ENG') return '🏴󠁧󠁢󠁥󠁮󠁧󠁿';
  if (code === 'GB-SCT') return '🏴󠁧󠁢󠁳󠁣󠁴󠁿';
  if (code === 'GB-WLS') return '🏴󠁧󠁢󠁷󠁬󠁳󠁿';
  const base = code.replace(/-.+$/, '');
  if (base.length !== 2) return '🏳️';
  return String.fromCodePoint(
    0x1f1e6 + base.charCodeAt(0) - 65,
    0x1f1e6 + base.charCodeAt(1) - 65,
  );
}

export function tTeam(name: string): string {
  return TEAM_NAMES[name] || name;
}

export function tVenue(name: string): string {
  if (!name) return '';
  for (const [key, cn] of Object.entries(VENUE_NAMES)) {
    if (name.includes(key) || key.includes(name)) return cn;
  }
  return name;
}
